# Problem: Search Insert Position
# Difficulty: Easy
# URL: https://leetcode.com/problems/search-insert-position/
# Runtime: 0 ms
# Memory: 12.9 MB

class Solution(object):
    def searchInsert(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: int
        """
        left, right = 0, len(nums) - 1
        
        while left <= right:
            mid = (left + right) // 2
            if nums[mid] == target:
                return mid
            elif nums[mid] < target:
                left = mid + 1
            else:
                right = mid - 1
        
        return left